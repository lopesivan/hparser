#ifndef OUTPUT_CONTROL_HPP
#define OUTPUT_CONTROL_HPP

//Class output_control----------------------------------------------------------
//Controls output function recursion

	/*! \class output_control
	    \brief Data output recursion control.
	    \ingroup output_group

	    This class provides recursion control to output interface classes.
	 */

class output_control
{
public:
	output_control();

protected:
	//Recursive control=====================================================

	/*! Determine if the element is currently sending its data.
	 */

	virtual
		bool
	sending_subs() const;

	/*! Change the sending status of the element.
	 */

	virtual
		bool
	set_sending_subs(bool) const;
	//======================================================================

private:
	mutable
		unsigned char
	sending_subsection;
	//Sending subsections to output
}; //END------------------------------------------------------------------------

#endif
